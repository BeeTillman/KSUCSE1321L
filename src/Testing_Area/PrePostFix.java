package Testing_Area;

public class PrePostFix {
    public static void main(String[] args){
        int x = 0;
        System.out.println(x++);
    }
}


/* NOTES */
/* ++ after a variable ("x++") will add AFTER the variable is used
IE, the above code will print "0", BUT if you were to loop it, the NEXT time, it would display "1"
 */